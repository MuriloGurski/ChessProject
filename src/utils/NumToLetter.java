package utils;

import java.util.Map;

public class NumToLetter {

    public static void numToLetter(int num){
        switch (num){
            case 0:
                System.out.print("a");
                break;
            case 1:
                System.out.print("b");
                break;
            case 2:
                System.out.print("c");
                break;
            case 3:
                System.out.print("d");
                break;
            case 4:
                System.out.print("e");
                break;
            case 5:
                System.out.print("f");
                break;
            case 6:
                System.out.print("g");
                break;
            case 7:
                System.out.print("h");
                break;
        }
    }
}
